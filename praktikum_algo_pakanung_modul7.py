def cek_prima(x):
    if x <= 1:
        return False
    if x == 2:
        return True
    for i in range(2,x):
      if x % i == 0:
        return False
    return True

def tampil_hasil_prima(angka, status_prima):
    if status_prima:
        print(angka, "adalah bilangan prima")
    else:
        print(angka, "bukan bilangan prima")

angka_input = int(input("Masukkan angka: "))
