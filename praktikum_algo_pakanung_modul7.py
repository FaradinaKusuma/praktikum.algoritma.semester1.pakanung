def dapatkan_akhiran_ordinal(n):
    if n == 0:
        return 'th'

    dua_digit_akhir = n % 100
    if 10 <= dua_digit_akhir <= 20:
        return 'th' #maksudnya akhir -1,-2,-3

    digit_akhir = n % 10
    if digit_akhir == 1:
        return 'st'
    elif digit_akhir == 2:
        return 'nd'
    elif digit_akhir == 3:
        return 'rd'
    else:
        return 'th'

